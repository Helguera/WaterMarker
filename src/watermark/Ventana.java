    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            watermark = selectedFile;
            BufferedImage img = null;
            try {
                img = ImageIO.read(new File(selectedFile.getAbsolutePath()));
            } catch (IOException e) {
                e.printStackTrace();
            }

            Image dimg = null;
            if (img.getWidth() > img.getHeight()) {
                dimg = img.getScaledInstance(jLabel1.getWidth(), img.getHeight() / (img.getWidth() / jLabel1.getWidth()), Image.SCALE_SMOOTH);
            } else {
                if (img.getWidth() < img.getHeight()) {
                    dimg = img.getScaledInstance(img.getWidth() / (img.getHeight() / jLabel1.getHeight()), jLabel1.getHeight(), Image.SCALE_SMOOTH);
                } else {
                    dimg = img.getScaledInstance(jLabel1.getWidth(), jLabel1.getHeight(), Image.SCALE_SMOOTH);
                }
            }

            //Image dimg = img.getScaledInstance(jLabel1.getWidth(), jLabel1.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon imageIcon = new ImageIcon(dimg);

            jLabel1.setIcon(imageIcon);
            jLabel1.repaint();
            jLabel1.revalidate();
        }

    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.setMultiSelectionEnabled(true);
        chooser.showOpenDialog(this);
        files = chooser.getSelectedFiles();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (int i = 0; i < files.length; i++) {
            model.addRow(new Object[]{files[i].getAbsolutePath()});
            model.setColumnIdentifiers(new Object[]{"Imágenes ("+files.length+")"});
            //jTextArea1.setText(jTextArea1.getText() + '\n' + files[i].getAbsolutePath());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if (jToggleButton1.isSelected()) {
            wm.setPosition(jToggleButton1.getName());
        } else {
            if (jToggleButton2.isSelected()) {
                wm.setPosition(jToggleButton2.getName());
            } else {
                if (jToggleButton3.isSelected()) {
                    wm.setPosition(jToggleButton3.getName());
                } else {
                    if (jToggleButton4.isSelected()) {
                        wm.setPosition(jToggleButton4.getName());
                    } else {
                        wm.setPosition(jToggleButton5.getName());
                    }
                }
            }
        }

        wm.setOpacity((double) jSpinner2.getValue());
        wm.setSize((int) jSpinner1.getValue());

        try {
            for (File file : files) {
                File salida = new File(file.getAbsolutePath().split("\\.")[0] + "_marca.png");
                try {
                    wm.addImageWatermark(watermark, "png", file, salida);
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Selecciona al menos una imagen", "Error", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(Ventana.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            JOptionPane.showMessageDialog(null, "Éxito!", "Info", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Selecciona logo e imagenes", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton7ActionPerformed

